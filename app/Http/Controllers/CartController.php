    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request, Product $product)
    {
        $cart = Cart::firstOrCreate([
            'user_id' => Auth::id(),
        ]);

        $item = $cart->items()->firstOrCreate(
            ['product_id' => $product->id],
            ['quantity' => 0]
        );

        $item->increment('quantity');

        return back();
    }

